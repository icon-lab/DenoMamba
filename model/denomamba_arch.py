def to_3d(x):
    return rearrange(x, 'b c h w -> b (h w) c')

def to_4d(x,h,w):
    return rearrange(x, 'b (h w) c -> b c h w',h=h,w=w)

class BiasFree_LayerNorm(nn.Module):
    def __init__(self, normalized_shape):
        super(BiasFree_LayerNorm, self).__init__()
        if isinstance(normalized_shape, numbers.Integral):
            normalized_shape = (normalized_shape,)
        normalized_shape = torch.Size(normalized_shape)

        assert len(normalized_shape) == 1

        self.weight = nn.Parameter(torch.ones(normalized_shape))
        self.normalized_shape = normalized_shape

    def forward(self, x):
        sigma = x.var(-1, keepdim=True, unbiased=False)
        return x / torch.sqrt(sigma+1e-5) * self.weight

class WithBias_LayerNorm(nn.Module):
    def __init__(self, normalized_shape):
        super(WithBias_LayerNorm, self).__init__()
        if isinstance(normalized_shape, numbers.Integral):
            normalized_shape = (normalized_shape,)
        normalized_shape = torch.Size(normalized_shape)

        assert len(normalized_shape) == 1

        self.weight = nn.Parameter(torch.ones(normalized_shape))
        self.bias = nn.Parameter(torch.zeros(normalized_shape))
        self.normalized_shape = normalized_shape

    def forward(self, x):
        mu = x.mean(-1, keepdim=True)
        sigma = x.var(-1, keepdim=True, unbiased=False)
        return (x - mu) / torch.sqrt(sigma+1e-5) * self.weight + self.bias


class LayerNorm(nn.Module):
    def __init__(self, dim, LayerNorm_type):
        super(LayerNorm, self).__init__()
        if LayerNorm_type =='BiasFree':
            self.body = BiasFree_LayerNorm(dim)
        else:
            self.body = WithBias_LayerNorm(dim)

    def forward(self, x):
        h, w = x.shape[-2:]
        return to_4d(self.body(to_3d(x)), h, w)


class FeedForward(nn.Module):
    def __init__(self, dim, ffn_expansion_factor, bias, n_cat):
        super(FeedForward, self).__init__()

        hidden_features = int(dim*ffn_expansion_factor)

        self.project_in = nn.Conv2d(dim * n_cat, hidden_features*2, kernel_size=1, bias=bias)

        self.dwconv = nn.Conv2d(hidden_features*2, hidden_features*2, kernel_size=3, stride=1, padding=1, groups=hidden_features*2, bias=bias)

        self.project_out = nn.Conv2d(hidden_features, dim, kernel_size=1, bias=bias)

    def forward(self, x):
        x = self.project_in(x)
        x1, x2 = self.dwconv(x).chunk(2, dim=1)
        x = F.gelu(x1) * x2
        x = self.project_out(x)
        return x



class MambaLayer(nn.Module):
    """ Mamba layer for state-space sequence modeling

    Args:
        dim (int): Model dimension.
        d_state (int): SSM state expansion factor.
        d_conv (int): Local convolution width.
        expand (int): Block expansion factor.
        transpose (bool): To decide channel or spatial attention.

    """

    def __init__(self, dim, d_state=16, d_conv=4, expand=2, transpose=False):
        super().__init__()
        self.dim = dim
        self.norm = nn.LayerNorm(dim)
        self.mamba = Mamba(
            d_model=dim,
            d_state=d_state,
            d_conv=d_conv,
            expand=expand,
            transpose=transpose
        )

    def forward(self, x):
        B, C, H, W = x.shape

        # Check model dimension
        assert C == self.dim

        # Convert input from (B, C, H, W) to (B, H*W, C)
        x = x.float().view(B, C, -1).permute(0, 2, 1)

        # Normalize and pass through Mamba layer
        norm_out = self.norm(x)
        mamba_out = self.mamba(norm_out)

        # Convert output from (B, H*W, C) to (B, C, H, W)
        out = mamba_out.permute(0, 2, 1).view(B, C, H, W)

        return out


class channel_compression(nn.Module):
    def __init__(self, in_channels, out_channels):
        """
        Args:
          in_channels (int):  Number of input channels.
          out_channels (int): Number of output channels.
          stride (int):       Controls the stride.
        """
        super(channel_compression, self).__init__()

        self.skip = nn.Sequential(
            nn.Conv2d(in_channels=in_channels, out_channels=out_channels, kernel_size=1, stride=1, bias=False))


        self.block = nn.Sequential(
            nn.Conv2d(in_channels=in_channels, out_channels=out_channels, kernel_size=3, padding=1, stride=1, bias=False),
            nn.Conv2d(in_channels=out_channels, out_channels=out_channels, kernel_size=3, padding=1, stride=1, bias=False))

    def forward(self, x):
        out = self.block(x)
        out += self.skip(x)
        return out

class FuseSSM(nn.Module):
    def __init__(self, dim, ffn_expansion_factor, bias, LayerNorm_type , num_heads = 0):
        super(FuseSSM, self).__init__()

        self.norm1 = LayerNorm(dim, LayerNorm_type)
        self.mamba_normal = MambaLayer(dim=dim, transpose=False)
        self.norm2 = LayerNorm(dim , LayerNorm_type)
        self.mamba_trp = MambaLayer(dim=dim, transpose=True)
        self.ffn = FeedForward(dim, ffn_expansion_factor, bias , 1)
        self.norm3 = LayerNorm(dim*1, LayerNorm_type)
        self.project_out = nn.Conv2d(in_channels=dim*3, out_channels=dim, kernel_size=1, bias=bias)
        self.cc = channel_compression(dim * 3, dim)  ## concat 2 tensor


    def forward(self, x):
        x_1 = self.mamba_normal(self.norm1(x))
        x_2 = self.mamba_trp(self.norm2(x))
        x_3 = self.ffn(self.norm3(x_2))
        x_cat = torch.cat((x_1, x_3, x), dim=1)
        x_cat2 = self.cc(x_cat)  # + x
        return x_cat2

